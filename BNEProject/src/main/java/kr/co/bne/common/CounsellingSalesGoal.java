package kr.co.bne.common;

public class CounsellingSalesGoal {
	private String employee_id;
	private int year;
	private int month;
	private int sales_goal;
	private int is_empty;
	public String getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getSales_goal() {
		return sales_goal;
	}
	public void setSales_goal(int sales_goal) {
		this.sales_goal = sales_goal;
	}
	public int getIs_empty() {
		return is_empty;
	}
	public void setIs_empty(int is_empty) {
		this.is_empty = is_empty;
	}
	
	
	
	
}
